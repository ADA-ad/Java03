package org.example;

public class Student {
    private String name;
    private int age;
    private String getClassName;
    public Student(String name, int age, String getClassName) {
    }
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getClassName() {
        return getClassName;
    }
}
